# operadores aritimétricos
# + - soma
# - - subtraçao
# \ - divisao
# * - multiplicaçao
# % - resto da divisao
# ** - elevado

numero1 = float(input("Digite o primeiro numero: "))
numero2 = float(input("Digite o segundo numero: "))
print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 % numero2)
print(numero1 ** numero2)

